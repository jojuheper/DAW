function languageAlert(language) {
  switch (language) {
    case 'es':
      console.log("Language: ES");
      alert("Bienvenido");
      break;
    case 'en':
      console.log("Language: EN");
      alert("Welcome");
      break;
    case 'de':
      console.log("Language: DE");
      alert("Willkommen");
      break;
  }
}
