document.write("parseInt(\"Pedro\") => " + parseInt("Pedro"));
document.write("</br>");
document.write("parseInt(15 + \"Pedro\") => " + parseInt(15 + "Pedro"));
document.write("</br>");
document.write("parseFloat(\"15.5\") => " + parseFloat("15.5"));